// Math.abs(x) la tra ve gia tri tuyet doi cua x
alert(Math.abs(-5.5));
alert(Math.abs(10)); 

// Math.celi(x) la lam tron len so nguyen gan nhat
alert(Math.ceil(4.4));
alert(Math.ceil(5.8));

// Math.floor(x) la lam tron xuong so nguyen gan nhat
alert(Math.floor(5.7));
alert(Math.floor(6.1));

// Math.round(x) la lam tron den so nguyen gan nhat
alert(Math.round(10.1));
alert(Math.round(20.6));